    template <class T>
    T *unserialize(UnSerializeStream *stream, Document *global_document)
    {
        T *obj = new T();
        obj->unserialize(stream, global_document);
        return obj;
    }
    template <class T>
    T *unserialize2(UnSerializeStream *stream, Function *function)
    {
        T *obj = new T();
        obj->unserialize(stream, function);
        return obj;
    }

    template <class T>
    T *unserialize3(UnSerializeStream *stream, Document *document, Function *function)
    {
        T *obj = new T();
        obj->unserialize(stream, document, function);
        return obj;
    }

    template <>
    JS::CSSKeyframeRuleValueArg *unserialize3<JS::CSSKeyframeRuleValueArg>(UnSerializeStream *stream, Document *global_document, Function *function)
    {
        JS::CSSKeyframeRuleValueArg *obj = new JS::CSSKeyframeRuleValueArg(true);
        obj->unserialize(stream, global_document, function);
        return obj;
    }

