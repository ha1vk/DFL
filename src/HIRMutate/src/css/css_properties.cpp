std::pair<string, Value *(*)(void)> HIRMutate::CSS::get_css_property(bool animatable)
{
    string prop;
    if (animatable)
        prop = CHOICE_OBJ(DOC::css_animatable_properties);
    else
        prop = CHOICE_OBJ(css_properties_key);
    return std::make_pair(prop, css_properties[prop]);
}

void *HIRMutate::CSS::get_css_property_value(const string &prop)
{
    return (void *)css_properties[prop];
}

string &HIRMutate::CSS::get_css_property_name()
{
    return CHOICE_OBJ(css_properties_key);
}

